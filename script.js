const productImagesMap = {};

function startImageCycle(el, id) {
  const imgs = productImagesMap[id];
  if (!imgs || imgs.length < 2) return;
  let idx = 0;
  el._imgInterval = setInterval(() => {
    idx = (idx + 1) % imgs.length;
    const img = el.querySelector('.product-img img');
    if (!img) return;
    img.style.opacity = '0';
    setTimeout(() => { img.src = imgs[idx]; img.style.opacity = '1'; }, 220);
  }, 4000);
}

function stopImageCycle(el, id) {
  clearInterval(el._imgInterval);
  delete el._imgInterval;
  const imgs = productImagesMap[id];
  if (!imgs) return;
  const img = el.querySelector('.product-img img');
  if (!img) return;
  img.style.opacity = '0';
  setTimeout(() => { img.src = imgs[0]; img.style.opacity = '1'; }, 220);
}


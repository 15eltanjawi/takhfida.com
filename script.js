let modalImgIdx = 0;
let modalImgList = [];

function updateModalSlider() {
  const img = document.getElementById('sliderImg');
  if (img) img.src = modalImgList[modalImgIdx];
  document.querySelectorAll('.sdot').forEach((d, i) => d.classList.toggle('active', i === modalImgIdx));
  document.querySelectorAll('.modal-thumb-img').forEach((d, i) => d.classList.toggle('active', i === modalImgIdx));
}

function slideModal(dir) {
  modalImgIdx = (modalImgIdx + dir + modalImgList.length) % modalImgList.length;
  updateModalSlider();
}

function goModalSlide(idx) {
  modalImgIdx = idx;
  updateModalSlider();
}

function openModal(id) {
  const t = translations[lang];
  const p = products[lang].find(x => x.id === id);
  modalImgList = (p.images && p.images.length) ? p.images : (p.img ? [p.img] : []);
  modalImgIdx = 0;

  const sliderHTML = modalImgList.length
    ? `<div class="modal-slider" style="background:${p.bg}">
        ${modalImgList.length > 1 ? `<button class="slider-arrow slider-prev" onclick="slideModal(-1)">&#8249;</button>` : ''}
        <img id="sliderImg" src="${modalImgList[0]}" alt="${p.name}" onerror="this.outerHTML='<span style=font-size:4rem>${p.emoji}</span>'" />
        ${modalImgList.length > 1 ? `<button class="slider-arrow slider-next" onclick="slideModal(1)">&#8250;</button>` : ''}
        ${modalImgList.length > 1 ? `<div class="slider-dots">${modalImgList.map((_, i) => `<span class="sdot${i === 0 ? ' active' : ''}" onclick="goModalSlide(${i})"></span>`).join('')}</div>` : ''}
      </div>`
    : `<div class="modal-emoji" style="background:${p.bg}"><span style="font-size:4rem">${p.emoji}</span></div>`;

  const thumbsHTML = modalImgList.length > 1
    ? `<div class="modal-thumbs-row">${modalImgList.map((img, i) =>
        `<img src="${img}" class="modal-thumb-img${i === 0 ? ' active' : ''}" onclick="goModalSlide(${i})" alt="" />`
      ).join('')}</div>`
    : '';

  document.getElementById('modalContent').innerHTML = `
    <div class="modal-two-col">
      <div class="modal-images-col">
        ${thumbsHTML}
        ${sliderHTML}
      </div>
      <div class="modal-info-col">
        <h2>${p.name}</h2>
        <div class="modal-col-price">${p.price} ${t.currency}</div>
        <p class="modal-col-desc">${p.longDesc || p.desc}</p>
        <ul class="modal-benefits">${p.benefits.map(b => `<li>${b}</li>`).join('')}</ul>
        <div class="modal-qty-row">
          <button class="qty-btn" onclick="document.getElementById('modalQty').stepDown()">−</button>
          <input id="modalQty" type="number" value="1" min="1" max="99" class="modal-qty-input" />
          <button class="qty-btn" onclick="document.getElementById('modalQty').stepUp()">+</button>
        </div>
        <button class="btn-modal-add-cart" onclick="addToCartQty(${p.id}); closeModal(); openOrderPopup(null);">${t.addToCart}</button>
        <button class="btn-modal-commander" onclick="openOrderPopup(${p.id})">
          🛒 ${lang === 'ar' ? 'اطلب الآن' : 'Commander Maintenant'}
        </button>
        <div class="modal-shipping-info">
          🚚 ${lang === 'ar' ? 'توصيل مجاني من 250 درهم' : "Livraison Gratuite dès 250 DH d'achat"}
        </div>
      </div>
    </div>
  `;
  document.getElementById('productModal').classList.add('open');
  document.getElementById('modalOverlay').classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closeModal() {
  document.getElementById('productModal').classList.remove('open');
  document.getElementById('modalOverlay').classList.remove('open');
  document.body.style.overflow = '';
}


function renderProducts(filter = 'all', overrideList = null) {
  currentFilter = filter;
  const grid = document.getElementById('productsGrid');
  const countEl = document.getElementById('productsCount');
  const t = translations[lang];
  const list = products[lang];
  let filtered = overrideList !== null
    ? overrideList
    : (filter === 'all' ? list : list.filter(p => p.category === filter));

  if (currentSort === 'price-asc')  filtered = [...filtered].sort((a,b) => a.price - b.price);
  if (currentSort === 'price-desc') filtered = [...filtered].sort((a,b) => b.price - a.price);
  if (currentSort === 'name-asc')   filtered = [...filtered].sort((a,b) => a.name.localeCompare(b.name));

  if (countEl) countEl.innerHTML = lang === 'fr'
    ? `Affichage de <strong>${filtered.length}</strong> produit${filtered.length !== 1 ? 's' : ''}`
    : `عرض <strong>${filtered.length}</strong> منتج`;

  if (!filtered.length) {
    grid.innerHTML = `<div class="search-no-results"><span>🔍</span>${t.searchNoResults}</div>`;
    return;
  }
  grid.innerHTML = filtered.map(p => {
    const thumb = (p.images && p.images.length) ? p.images[0] : (p.img || null);
    productImagesMap[p.id] = (p.images && p.images.length > 1) ? p.images : null;
    return `
    <div class="product-card" onclick="openModal(${p.id})" onmouseenter="startImageCycle(this,${p.id})" onmouseleave="stopImageCycle(this,${p.id})">
      <div class="product-img">
        ${thumb
          ? `<img src="${thumb}" alt="${p.name}" loading="lazy" onerror="this.style.display='none';this.nextElementSibling.style.display='flex'" /><span class="product-emoji" style="display:none">${p.emoji}</span>`
          : `<span class="product-emoji">${p.emoji}</span>`}
        ${p.badge ? `<span class="product-badge">${p.badge}</span>` : ''}
        <div class="card-hover-overlay">
          <button class="card-add-btn" onclick="event.stopPropagation(); addToCart(${p.id})">${t.addBtn}</button>
        </div>
      </div>
      <div class="product-card-bottom">
        <h3>${p.name}</h3>
        <span class="card-price">${p.price} ${t.currency}</span>
      </div>
    </div>
  `; }).join('');
}


